no=int(input("Enter day number: "))
 
if no==1:
    print("Monday")
if no==2:
    print("Tuesday")
if no==3:
    print("Wednesday")
if no==4:
    print("Thursday")
if no==5:
    print("Friday")
if no==6:
    print("Saturday")
if no==7:
    print("Sunday")
if no>7:
    print("Invalid input")